package ca.aquiletour.data;

public class Student extends User {
	

	protected String registrationId; // XXX: confidential, do NOT send to client
	protected String authToken;      // XXX: confidential, send ONLY in a cookie

	public Student(Student student) {
		super(student);
		this.registrationId = student.getRegistrationId();
		this.authToken = student.getAuthToken();
	}

	public String getRegistrationId() {
		return registrationId;
	}

	public void setRegistrationId(String registrationId) {
		this.registrationId = registrationId;
	}

	public String getAuthToken() {
		return authToken;
	}

	public void setAuthToken(String authToken) {
		this.authToken = authToken;
	}
}
